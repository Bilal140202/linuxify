describe('customTemplate', () => {
  it('preserves the user-provided shebang as the first line', () => {
    const out = customTemplate(CUSTOM_SPEC_WITH_SHEBANG);
    expect(out.split('\n')[0]).toBe('#!/bin/bash');
  });

  it('inserts the Linuxify header after the user shebang', () => {
    const out = customTemplate(CUSTOM_SPEC_WITH_SHEBANG);
    const lines = out.split('\n');
    expect(lines[0]).toBe('#!/bin/bash');
    expect(lines[1]).toBe('# Auto-generated by Linuxify (custom variant). Do not edit.');
  });

  it('includes the package name and distro in the header', () => {
    const out = customTemplate(CUSTOM_SPEC_WITH_SHEBANG);
    expect(out).toContain('# Package: mytool');
    expect(out).toContain('# Distro: alpine');
    expect(out).toContain('# Variant: custom');
  });

  it('includes the Linuxify header signature', () => {
    const out = customTemplate(CUSTOM_SPEC_WITH_SHEBANG);
    expect(out).toContain(LINUXIFY_HEADER_SIGNATURE);
  });

  it('preserves the user-provided script body verbatim', () => {
    const out = customTemplate(CUSTOM_SPEC_WITH_SHEBANG);
    expect(out).toContain('echo "pre"');
    expect(out).toContain('exec linuxify run mytool -- "$@"');
  });

  it('prepends the Termux sh shebang when the user script lacks one', () => {
    const out = customTemplate(CUSTOM_SPEC_NO_SHEBANG);
    expect(out.split('\n')[0]).toBe(LAUNCHER_SHEBANG);
    expect(out.split('\n')[0]).toBe('#!/data/data/com.termux/files/usr/bin/sh');
  });

  it('inserts the header after the prepended shebang when the user script lacks one', () => {
    const out = customTemplate(CUSTOM_SPEC_NO_SHEBANG);
    const lines = out.split('\n');
    expect(lines[0]).toBe('#!/data/data/com.termux/files/usr/bin/sh');
    expect(lines[1]).toBe('# Auto-generated by Linuxify (custom variant). Do not edit.');
  });

  it('preserves the user-provided script body when no shebang is present', () => {
    const out = customTemplate(CUSTOM_SPEC_NO_SHEBANG);
    expect(out).toContain('echo hello');
    expect(out).toContain('exec linuxify run bare -- "$@"');
  });

  it('adds a trailing newline if the user script lacks one', () => {
    const spec: LauncherSpec = {
      packageName: 'p',
      launcherName: 'p',
      distro: 'ubuntu',
      variant: 'custom',
      customScript: 'echo hello',
    };
    const out = customTemplate(spec);
    expect(out.endsWith('\n')).toBe(true);
    expect(out.endsWith('\n\n')).toBe(false);
  });

  it('does not add a duplicate trailing newline if the user script already ends with one', () => {
    const spec: LauncherSpec = {
      packageName: 'p',
      launcherName: 'p',
      distro: 'ubuntu',
      variant: 'custom',
      customScript: 'echo hello\n',
    };
    const out = customTemplate(spec);
    expect(out.endsWith('\n')).toBe(true);
    expect(out.endsWith('\n\n')).toBe(false);
  });

  it('throws LauncherError(E_LAUNCHER_CUSTOM_SCRIPT_MISSING) when customScript is missing', () => {
    const spec: LauncherSpec = {
      packageName: 'p',
      launcherName: 'p',
      distro: 'ubuntu',
      variant: 'custom',
    };
    expect(() => customTemplate(spec)).toThrow(LauncherError);
    expect(() => customTemplate(spec)).toThrow(/customScript/);
  });

  it('throws LauncherError(E_LAUNCHER_CUSTOM_SCRIPT_MISSING) when customScript is empty', () => {
    const spec: LauncherSpec = {
      packageName: 'p',
      launcherName: 'p',
      distro: 'ubuntu',
      variant: 'custom',
      customScript: '',
    };
    expect(() => customTemplate(spec)).toThrow(LauncherError);
  });

  it('throws LauncherError(E_LAUNCHER_CUSTOM_SCRIPT_MISSING) when customScript is whitespace-only', () => {
    const spec: LauncherSpec = {
      packageName: 'p',
      launcherName: 'p',
      distro: 'ubuntu',
      variant: 'custom',
      customScript: '   \n\t  ',
    };
    expect(() => customTemplate(spec)).toThrow(LauncherError);
  });

  it('includes a "begin user-provided script" separator line', () => {
    const out = customTemplate(CUSTOM_SPEC_WITH_SHEBANG);
    expect(out).toContain('# --- begin user-provided script ---');
  });

  it('handles a user script that is only a shebang line (no body)', () => {
    const spec: LauncherSpec = {
      packageName: 'p',
      launcherName: 'p',
      distro: 'ubuntu',
      variant: 'custom',
      customScript: '#!/bin/sh',
    };
    const out = customTemplate(spec);
    const lines = out.split('\n');
    expect(lines[0]).toBe('#!/bin/sh');
    expect(lines[1]).toBe('# Auto-generated by Linuxify (custom variant). Do not edit.');
  });
});

// ---------------------------------------------------------------------------
// Cross-variant invariants
// ---------------------------------------------------------------------------

describe('cross-variant invariants', () => {
  it('all three variants include the Linuxify header signature', () => {
    const std = standardTemplate(STANDARD_SPEC);
    const dir = directTemplate(DIRECT_SPEC, '/usr/bin/eslint');
    const cus = customTemplate(CUSTOM_SPEC_WITH_SHEBANG);
    expect(std).toContain(LINUXIFY_HEADER_SIGNATURE);
    expect(dir).toContain(LINUXIFY_HEADER_SIGNATURE);
    expect(cus).toContain(LINUXIFY_HEADER_SIGNATURE);
  });

  it('all three variants end with exactly one trailing newline', () => {
    const std = standardTemplate(STANDARD_SPEC);
    const dir = directTemplate(DIRECT_SPEC, '/usr/bin/eslint');
    const cus = customTemplate(CUSTOM_SPEC_WITH_SHEBANG);
    for (const out of [std, dir, cus]) {
      expect(out.endsWith('\n')).toBe(true);
      expect(out.endsWith('\n\n')).toBe(false);
    }
  });

  it('all three variants forward arguments via "$@"', () => {
    const std = standardTemplate(STANDARD_SPEC);
    const dir = directTemplate(DIRECT_SPEC, '/usr/bin/eslint');
    const cus = customTemplate(CUSTOM_SPEC_WITH_SHEBANG);
    expect(std).toContain('"$@"');
    expect(dir).toContain('"$@"');
    expect(cus).toContain('"$@"');
  });
});
